// Função para fazer o download do currículo
function downloadResume() {
    // Caminho relativo para o arquivo PDF do currículo no mesmo diretório do projeto
    const resumePath = 'resume.pdf';

    // Cria um elemento "a" (link)
    const link = document.createElement('a');
    link.href = resumePath;
    link.target = '_blank'; // Abre em uma nova aba
    link.download = 'resume_JoaoPedro.pdf'; // Define o nome do arquivo de download

    // Clica no link automaticamente
    link.click();
}


// Adiciona um ouvinte de evento ao botão de download
const downloadButton = document.getElementById('downloadButton');
downloadButton.addEventListener('click', downloadResume);

